# If statement to check number as postive number, negative
# or a zero.

# Get user input
num = int(input('Enter a number: ')) 

# Computation: to check the number as positive, negative or zero
# using multiple if-statement.
# Print the output decision from the if-statement.

# Check if the input number is a positive number
if num > 0:
    print('Your number is positive')

# Check if the input number is a negative number
if num < 0:
    print('Your number is negative')

# Check if the input number is a zero
if num == 0:
    print('Your number is zero')

# The end of the program
print('Finished')
